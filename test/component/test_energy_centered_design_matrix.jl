# Regression test for the energy-centered-design-matrix refactor.
#
# The reference `(j0, jphi)` literals below were captured by
# `tools/personal/capture_energy_centered_refs.jl` from the current
# bias-in-`X` code, before any source change. They pin the
# pre-refactor floating-point output for two integration datasets
# (`febcc_2x2x2_pm`, `fege_2x2x2`) across four `(estimator, weight)`
# pairs each.
#
# The refactor (energy-only centering inside
# `assemble_weighted_problem` + `extract_j0_jphi` with no bias-column
# discard) is mathematically equivalent to the bias-in-`X`
# formulation, but the floating-point operation sequence differs.
# Agreement is therefore expected within rounding noise rather than
# bit-for-bit. The tolerance is measured in M3 and recorded in
# `REGRESSION_RTOL` below; the expected order of magnitude is
# `rtol ~= 1e-10`.
#
# In M1 the comparison `@test`s are wrapped in `@test_skip` and the
# file is intentionally NOT in `test/runtests.jl`. M3 swaps the
# wrappers to plain `@test`, populates `REGRESSION_RTOL`, and adds
# the file to the unit-test runner.


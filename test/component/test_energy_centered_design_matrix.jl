# In M1, the comparison `@test`s are wrapped in `@test_skip`. The
# `_ec_build_dataset` / `_ec_fit` helpers are referenced inside the
# skipped expressions and are NOT evaluated yet, so no heavy basis
# construction runs while this file is excluded from the runner.
# M3 swaps `@test_skip` -> `@test`, sets `REGRESSION_RTOL`, and adds
# this file to `test/runtests.jl`.
@testset "Energy-centered design matrix regression" begin
    @testset "febcc_2x2x2_pm" begin
        dataset = _ec_build_dataset("febcc_2x2x2_pm")
        for (label, est_thunk, w, j0_ref, jphi_ref) in _FEBCC_CASES
            f = _ec_fit(dataset, est_thunk(), w)
            @test_skip isapprox(intercept(f), j0_ref; rtol = REGRESSION_RTOL)
            @test_skip isapprox(coef(f), jphi_ref; rtol = REGRESSION_RTOL)
        end
    end

    @testset "fege_2x2x2" begin
        dataset = _ec_build_dataset("fege_2x2x2")
